    public function getPage(Request $request)
    {
        $columns = $request->columns;
        $length = $request->length;
        $order = $request->order;
        $search = $request->search;
        $start = $request->start;
        $page = $start / $length + 1;

        $member_id = $request->member_id;

        $col = array('id', 'code', 'member_id', 'in_from', 'out_to', 'reference_id', 'detail', 'amount', 'type', 'status', 'create_by', 'update_by', 'created_at', 'updated_at', 'deleted_at');
        $orderby = array('', 'code', 'member_id', 'in_from', 'out_to', 'reference_id', 'detail', 'amount', 'type', 'status', 'create_by', 'update_by', 'created_at', 'updated_at');


        $D = WalletTransaction::select($col);

        if (isset($member_id)) {
            $D->where('member_id', $member_id);
        }else{
            $D->where('member_id', "");
        }

        if ($orderby[$order[0]['column']]) {
            $D->orderby($orderby[$order[0]['column']], $order[0]['dir']);
        }

        if ($search['value'] != '' && $search['value'] != null) {

            $D->Where(function ($query) use ($search, $col) {

                //search datatable
                $query->orWhere(function ($query) use ($search, $col) {
                    foreach ($col as &$c) {
                        $query->orWhere($c, 'like', '%' . $search['value'] . '%');
                    }
                });

                //search with
                // $query = $this->withPermission($query, $search);
            });
        }

        $d = $D->paginate($length, ['*'], 'page', $page);

        if ($d->isNotEmpty()) {

            //run no
            $No = (($page - 1) * $length);

            for ($i = 0; $i < count($d); $i++) {

                $No = $No + 1;
                $d[$i]->No = $No;
                $d[$i]->member = member::find($d[$i]->member_id);
            }
        }

        return $this->returnSuccess('เรียกดูข้อมูลสำเร็จ', $d);
    }
